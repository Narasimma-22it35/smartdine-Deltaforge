// Calculate distance between two coordinates (Haversine formula)
export const calculateDistance = (lat1: number, lng1: number, lat2: number, lng2: number): number => {
  const R = 6371; // Earth's radius in km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) * 
    Math.sin(dLng/2) * Math.sin(dLng/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
};

// Get restaurants near a location
export const getNearbyRestaurants = (
  userLat: number, 
  userLng: number, 
  maxDistanceKm: number = 50
): Restaurant[] => {
  return indianRestaurants
    .map(restaurant => {
      const distance = calculateDistance(
        userLat, 
        userLng, 
        restaurant.coordinates.lat, 
        restaurant.coordinates.lng
      );
      return { 
        ...restaurant, 
        distance: distance < 1 ? `${Math.round(distance * 1000)} m` : `${distance.toFixed(1)} km`,
        distanceValue: distance 
      };
    })
    .filter(r => r.distanceValue <= maxDistanceKm)
    .sort((a, b) => a.distanceValue - b.distanceValue);
};

// Get closest city based on coordinates
export const getClosestCity = (lat: number, lng: number): string => {
  let closestCity = indianCities[0].name;
  let minDistance = Infinity;
  
  indianCities.forEach(city => {
    const distance = calculateDistance(lat, lng, city.lat, city.lng);
    if (distance < minDistance) {
      minDistance = distance;
      closestCity = city.name;
    }
  });
  
  return closestCity;
};
